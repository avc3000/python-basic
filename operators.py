### Operators for python ###
print(3 + 4)
print(3 - 5)
print(3 * 6)
print(3 / 7)
print(10 % 3)
print(10 // 8)
print(10 ** 3)
print(10 ** 4 * 2 / 100 + 50 // 3)

print("Hello " + "Python")
print("You have " + str(5) + " apples")
print("Hello " * 5)

# Comparative Operators
print(3 < 4)
print(3 > 4)
print(3 <= 4)
print(3 >= 4)
print(3 == 4)
print(3 != 4)

# Alphabetical sort
print("Hello" < "Python")
print("Hello" > "Python")
print("Hello" <= "Python")
print("Hello" >= "Python")
print("Hello" == "Python")
print("Hello" != "Python")

# Number of characters
print(len("Hello") < len("Python"))
print(len("Hello") > len("Python"))
print(len("Hello") != len("Python"))

# Logics Operators
print(3 > 4 and "Hello" > "Python")
print(3 > 4 or "Hello" != "Python")
print(3 > 4 or ("Hello" <= "Python" and 4 == 4))
print(not ("Hello" == "Python"))
